package comparisonOperators;

public class CompareNumbers {

	//class level variables
	int num1, num2;
	boolean areTwoNumbersEqual;

	// Comparison Operators

	// To compare two numbers are equal - we use ==

	// To compare two numbers are not equal - we use !=

	// Result of any comparison is always of type boolean

	void compareNumbersAreEqual() {
		
		areTwoNumbersEqual = num1 == num2;
		System.out.println("num1 is equal to num2 : " + areTwoNumbersEqual);

	}
	
		
	
}
